def _merge_phrase_group(group: str) -> PhraseTuple:
    out = []
    seen = set()

    for pack in (
        ZH_HANS_MEMORY_RESUME_LEXICON,
        EN_MEMORY_RESUME_LEXICON,
    ):
        for phrase in pack.get(group, ()):
            text = str(phrase).strip()
            if not text or text in seen:
                continue
            seen.add(text)
            out.append(text)

    return tuple(out)


MEMORY_RESUME_I18N_LEXICON: Dict[str, PhraseTuple] = {
    group: _merge_phrase_group(group)
    for group in _MEMORY_RESUME_I18N_LEXICON_GROUPS
}


def get_memory_resume_phrases(group: str) -> PhraseTuple:
    key = str(group or "").strip()
    return MEMORY_RESUME_I18N_LEXICON.get(key, ())


def get_memory_resume_phrase_set(group: str) -> FrozenSet[str]:
    return frozenset(get_memory_resume_phrases(group))


